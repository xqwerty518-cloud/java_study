package 금요일;

public class 변수와자료형 {

	public static void main(String[] args) {
		
		/**
		* 자료형 변수명;
		* 
		* 자료형은 사용하기 전에 선언해야 한다. 만약 사용후 선언한다면 에러가 걸림.
		* 
		* 또 다른 자료형으로 같은 변수명을 사용할 수 없다.
		*/
		
		int a =3;
		b = 5; // 자료형을 선언하지 않았기에 에러
		String c;
		c = "안녕";
		
		double a = 3.5; // 이미 int형의 a라는 변수가 선언되었기에 에러 
		
		// 변수 선언과 값을 대입하는 2가지 방법
		int d = 3; // 변수 선언과 함께 값 대입
		int e; // 변수 선언 후
		e = 4; // 값 대입
		
		
	}

}

